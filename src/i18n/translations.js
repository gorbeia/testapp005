// This is the dictionary for Aditzak's *interface* language — menus, buttons,
// instructions, feedback messages, and the English glosses attached to verbs/
// grammatical persons. It is independent from the Basque (Euskara) content
// being taught, which is never translated.
//
// `LANGUAGES` lists the supported interface languages, in their own native
// name (shown as-is in the language switcher, regardless of the current
// language). `DEFAULT_LANGUAGE` is the fallback used for any key missing from
// the active language's dictionary, and for browser-language detection that
// doesn't match a supported language.
//
// Keys ending in `_one`/`_other` are picked via `tCount` based on a count
// (English/Spanish singular vs. plural; Basque doesn't inflect the noun after
// a number, so both forms are identical there).
// =============================================================================

